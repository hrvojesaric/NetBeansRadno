    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        Momcad s = new Momcad();
      
       if(!kontrola(s)){
           return;
       }
        
        try {
            obradaMomcad.create(s);
        } catch (ZupanijskaNogometnaLigaException e) {
            JOptionPane.showConfirmDialog(null, e.getPoruka());
            return;
        }
        
        ucitajMomcadi();
        
        ocistiPolja();
        
        
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
         Momcad s = lstMomcadi.getSelectedValue();
         
         if(s==null){
            JOptionPane.showConfirmDialog(null, "Prvo odaberite smjer");
        }
        
        
         if(!kontrola(s)){
           return;
       }
        
        try {
            obradaMomcad.update(s);
        } catch (ZupanijskaNogometnaLigaException e) {
            JOptionPane.showConfirmDialog(null, e.getPoruka());
            return;
        }
        
        ucitajMomcadi();
        
        ocistiPolja();
    }//GEN-LAST:event_btnPromjenaActionPerformed

    private void btnBrisanjeiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrisanjeiActionPerformed
        Momcad s = lstMomcadi.getSelectedValue();
        
        if(s==null){
            JOptionPane.showConfirmDialog(null, "Prvo odaberite smjer");
        }
        
        if(obradaMomcad.delete(s)){
            ucitajMomcadi();
            ocistiPolja();
        }else{
            JOptionPane.showConfirmDialog(null,"Momčad nije obrisana jer ima igrače");
        }
    }//GEN-LAST:event_btnBrisanjeiActionPerformed

    private void lstMomcadiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstMomcadiValueChanged
     
        if(evt.getValueIsAdjusting()){
            return;
        }
        
        Momcad s = lstMomcadi.getSelectedValue();
        if(s==null){
            return;
        }
        txtNaziv.setText(s.getNaziv());
        txtFizioterapeut.setText(s.getFizioterapeut());
        txtTrener.setText(s.getTrener());
        txtStadion.setText(s.getStadion());
        txtPredstavnikKluba.setText(s.getPredstavnik_kluba());
    }//GEN-LAST:event_lstMomcadiValueChanged
                                          
        
        
        
    

