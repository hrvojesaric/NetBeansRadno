    private void ucitajMomcadi() {
        
        
        DefaultListModel<Momcad> m = new DefaultListModel<>();
                
        for(Momcad s : obradaMomcad.read()){
            m.addElement(s);
        }
        lstMomcadi.setModel(m);
    }
    
private void ocistiPolja(){
    txtTrener.setText("");
       txtNaziv.setText("");
       txtStadion.setText("");
       txtPredstavnikKluba.setText("");
       txtFizioterapeut.setText("");
}


private boolean kontrola(Momcad s) {
    s.setNaziv(txtNaziv.getText());
    s.setFizioterapeut(txtFizioterapeut.getText());
    s.setTrener(txtTrener.getText());
    s.setPredstavnik_kluba(txtPredstavnikKluba.getText());
    s.setStadion(txtStadion.getText());
    return true;
    }
